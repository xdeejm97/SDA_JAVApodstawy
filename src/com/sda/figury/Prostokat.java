package com.sda.figury;

public class Prostokat {
    public Prostokat(double a, double b) {
        this.a = a;
        this.b = b;
    }

    private double a;
    private double b;

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }
}
